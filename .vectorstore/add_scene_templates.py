def add_templates_to_graph() -> None:
    """添加场景模板到知识图谱"""

    # 读取现有知识图谱
    with open(KNOWLEDGE_GRAPH_FILE, "r", encoding="utf-8") as f:
        kg_data = json.load(f)

    entities = kg_data.get("实体", {})

    # 添加场景模板
    added_count = 0
    for template in SCENE_TEMPLATES:
        template_id = template["id"]
        if template_id not in entities:
            entities[template_id] = template
            added_count += 1
            print(f"  [OK] 添加模板: {template['名称']}")
        else:
            print(f"  [SKIP] 已存在: {template['名称']}")

    # 添加情绪状态对照表（作为特殊实体）
    emotion_entity = {
        "id": "emotion_states_reference",
        "名称": "情绪状态对照表",
        "类型": "参考数据",
        "属性": EMOTION_STATES,
    }
    if "emotion_states_reference" not in entities:
        entities["emotion_states_reference"] = emotion_entity
        added_count += 1
        print(f"  [OK] 添加情绪状态对照表")

    # 保存更新后的知识图谱
    with open(KNOWLEDGE_GRAPH_FILE, "w", encoding="utf-8") as f:
        json.dump(kg_data, f, ensure_ascii=False, indent=2)

    print(f"\n[完成] 已添加 {added_count} 个新实体")


def main():
    print("=" * 60)
    print("添加场景预判模板到知识图谱")
    print("=" * 60)

    print("\n[1/1] 添加16种场景模板...")
    add_templates_to_graph()

    print("\n" + "=" * 60)
    print("添加完成！")
    print("=" * 60)


if __name__ == "__main__":
    main()
