def add_tech_to_graph() -> None:
    """添加技术基础实体到知识图谱"""

    # 读取现有知识图谱
    with open(KNOWLEDGE_GRAPH_FILE, "r", encoding="utf-8") as f:
        kg_data = json.load(f)

    entities = kg_data.get("实体", {})

    # 添加技术基础实体
    added_count = 0
    for tech in TECH_ENTITIES:
        tech_id = tech["id"]
        if tech_id not in entities:
            entities[tech_id] = tech
            added_count += 1
            print(f"  [OK] 添加: {tech['名称']} ({tech['属性']['文明']})")
        else:
            print(f"  [SKIP] 已存在: {tech['名称']}")

    # 保存更新后的知识图谱
    with open(KNOWLEDGE_GRAPH_FILE, "w", encoding="utf-8") as f:
        json.dump(kg_data, f, ensure_ascii=False, indent=2)

    print(f"\n[完成] 已添加 {added_count} 个技术基础实体")


